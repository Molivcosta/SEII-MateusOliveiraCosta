#5.	Arithmetic Operators

print(10 + 3)   #Soma
print(10 - 3)   #Subtração
print(10 * 3)   #Multiplicação
print(10 / 3)   #Divisão em ponto flutuante
print(10 // 3)  #Divisão inteira
print(10 % 3)   #Módulo - Retorna o resto da divisão
print(10 ** 3)  #Expoente 10^3

x = 10
x += 3          #Operador de atribuição aumentada





















