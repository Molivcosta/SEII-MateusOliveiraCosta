#8.	Logical Operators

price = 25
print(price > 10 and price < 30)       #Operador de lógico & ou and retorna true quando toda expressão é verdadeira

price = 5
print(price > 10 or price < 30)        #Operador de lógico ||, ou, or true se pelo menos uma expressão é verdadeira

price = 5
print( not price > 10)                 #Operador de lógico not (negação ou barrado) inverte qualquer valor