#13. List Methods

numbers = [1, 2, 3, 4, 5]
numbers.append(6)   #Acrescenta o número escolhido no final da lista
print(numbers)

numbers.insert(0, -1)   #Acrescenta o número escolhido em qualquer posição da lista
print(numbers)

numbers.remove(3)   #Remove o número escolhido da lista
print(numbers)

print(1 in numbers) #Retorna uma expressão bool

print(len(numbers)) #Retorna o tamanho da lista

numbers.clear()   #limpa da lista
print(numbers)





