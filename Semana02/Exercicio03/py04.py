#4. Strings - Manipulando strings

course = 'Python for Beginners'
print(course.upper())                       #upper - Retorna uma nova string em maiúscula
print(course.find('y'))                     #find -  Rotorna a posição do caracter pesquisado
print(course.replace('y', 'i'))   #replace -  Substitui uma string por outra
print("Python" in course)   #Retorna um valor booleano referente a palavra pesquisada