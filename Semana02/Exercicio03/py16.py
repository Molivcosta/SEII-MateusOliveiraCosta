#16. Tuples - São como listas, porém são imutáveis

numbers = (1, 2, 3, 3)     #Os números entre colchetes fazem parte de um tupla, (...)
print(numbers)
print(numbers.count(3))           #Retorna a quantidade de repetições do número escolhido em uma tupla
print(numbers.index(3))           #Retorna o índice da primeira ocorrência do elemento fornecido em uma tupla
