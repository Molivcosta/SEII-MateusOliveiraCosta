#7.	Comparison Operators

x = 3 > 2   #Operador de comparação maior
print(x)

x = 3 < 2   #Operador de comparação menor
print(x)

x = 3 <= 2   #Operador de comparação menor igual
print(x)

x = 3 >= 2   #Operador de comparação maior igual
print(x)

x = 3 == 2   #Operador de comparação de igualdade
print(x)

x = 3 != 2   #Operador de comparação não igual ou diferente
print(x)